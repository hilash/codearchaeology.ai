void List::InsertFront (Person man) {
	Node* p;
	p=new Node;
	p->current=man;
	p->prev=NULL;
	if (first!= NULL){
		first->prev=p;
	}
	else {
		last=p;
	}
	p->next=first;
	first=p;
}

void List::InsertBack (Person man) {
	Node* p;
	p=new Node;
	p->current=man;
	p->prev=last;
	p->next=NULL;
	if (last!=NULL){
		last->next=p;
	}
	else {
		first=p;
	}
	last=p;
	
}

void List::RemoveFront () {
	if (first!=NULL){
		Node* temp=first->next;
		if(first->next!=NULL){
		(first->next->prev)=NULL;
		}
		delete first;
		first=temp;
	}
}


void List::RemoveBack () {
	if (last!=NULL) {
		Node* temp=last->prev;
		if (last->prev!=NULL) {
			last->prev->next=NULL;
		}
		delete last;
		last=temp;
	}
}


